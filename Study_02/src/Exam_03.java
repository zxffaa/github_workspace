
public class Exam_03 {
	public static void main(String[] args) {
		int i=1;
		int s=0;
		while(i<=5) {
			System.out.println(i);
			s=s+i;
			i++;
		}
		System.out.println(s);
		System.out.println("------------");
		int j=0;
		int t=0;
		while(j<5) {
			j++;
			System.out.println(j);
			t=t+j;
		}
		System.out.println(t);
	}

}
