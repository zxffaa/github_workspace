//shift 연산자(<<, >>)--> 비트이동연산자
public class Exam_09 {
	public static void main(String[] args) {
		int a = 1;
		System.out.println(a);//1
		int b = a<<1;
		System.out.println(b);//2
		b = a<<2;
		System.out.println(b);//4
		b = a<<3;
		System.out.println(b);//8
		b = a<<4;
		System.out.println(b);//16

	}

}
