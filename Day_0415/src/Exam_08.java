/*   printf("문자열 %변환기호", 출력할 값)
 *   %d(정수), %f(실수),%s(문자열), %n(newLine)
 */
public class Exam_08 {
	public static void main(String[] args) {
		System.out.printf("Hello World\n");//출력후 줄바꿈
		System.out.printf("Hello World%n");//출력후 줄바꿈
		System.out.printf("정수 = %d\n", 100);
		System.out.printf("실수 = %f%n", 3.14);
		System.out.printf("문자열 = %s\n", "Hell Korea");
		System.out.printf("국어=%d, 영어=%d \n", 90,80);
		

		

		
		
	}

}
